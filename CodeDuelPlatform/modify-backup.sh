#!/bin/bash

# Этот скрипт нужно запустить на сервере для модификации файла бэкапа
# Он извлекает только INSERT-запросы из оригинального файла бэкапа

# Путь к файлу бэкапа
BACKUP_FILE="CodeDuelPlatform/testai_backup.sql"

# Создаем временный файл
TMP_FILE=$(mktemp)

# Добавляем заголовок
echo "-- Только INSERT-запросы из оригинального бэкапа" > $TMP_FILE

# Извлекаем только INSERT-запросы
grep -i "^INSERT INTO" $BACKUP_FILE >> $TMP_FILE

# Заменяем оригинальный файл
mv $TMP_FILE $BACKUP_FILE

echo "Файл бэкапа успешно модифицирован. Теперь он содержит только INSERT-запросы." 